class PerformanceTracker {
    constructor() {
        this.timers = new Map();
    }
    
    start(label) {
        this.timers.set(label, Date.now());
    }
    
    end(label, metadata = {}) {
        const start = this.timers.get(label);
        if (!start) {
            logger.warn(`Performance timer '${label}' was not started`);
            return;
        }
        
        const duration = Date.now() - start;
        this.timers.delete(label);
        
        logger.info(`${label} completed`, {
            category: 'performance',
            duration,
            ...metadata
        });
        
        // –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ –æ –º–µ–¥–ª–µ–Ω–Ω—ã—Ö –æ–ø–µ—Ä–∞—Ü–∏—è—Ö
        if (duration > 1000) {
            logger.warn(`Slow operation: ${label}`, {
                category: 'performance',
                duration,
                ...metadata
            });
        }
        
        return duration;
    }
}

const performance = new PerformanceTracker();

// –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞
async function logServerStats() {
    const usage = process.memoryUsage();
    const stats = {
        category: 'system',
        event: 'server_stats',
        memory: {
            rss: Math.round(usage.rss / 1024 / 1024) + 'MB',
            heapTotal: Math.round(usage.heapTotal / 1024 / 1024) + 'MB',
            heapUsed: Math.round(usage.heapUsed / 1024 / 1024) + 'MB',
            external: Math.round(usage.external / 1024 / 1024) + 'MB'
        },
        uptime: Math.round(process.uptime() / 60) + ' minutes',
        nodeVersion: process.version,
        platform: process.platform
    };
    
    logger.info('Server statistics', stats);
}

